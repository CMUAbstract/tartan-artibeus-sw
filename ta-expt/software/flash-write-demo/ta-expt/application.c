//// Fills rx_cmd_buff with bootloader_write_data cmd given page ID and data
void set_bootloader_write_data_cmd(
 rx_cmd_buff_t* rx_cmd_buff_o,
 const uint8_t subpage_id,
 const uint8_t* subpage_data
) {
  clear_rx_cmd_buff(rx_cmd_buff_o);
  rx_cmd_buff_o->end_index = 138;
  rx_cmd_buff_o->data[START_BYTE_0_INDEX] = START_BYTE_0;
  rx_cmd_buff_o->data[START_BYTE_1_INDEX] = START_BYTE_1;
  rx_cmd_buff_o->data[MSG_LEN_INDEX] = ((uint8_t)0x87);
  rx_cmd_buff_o->data[HWID_LSB_INDEX] = ((uint8_t)0x01);
  rx_cmd_buff_o->data[HWID_MSB_INDEX] = ((uint8_t)0x00);
  rx_cmd_buff_o->data[MSG_ID_LSB_INDEX] = ((uint8_t)0x01);
  rx_cmd_buff_o->data[MSG_ID_MSB_INDEX] = ((uint8_t)0x00);
  rx_cmd_buff_o->data[DEST_ID_INDEX] = LST;
  rx_cmd_buff_o->data[OPCODE_INDEX] = BOOTLOADER_WRITE_PAGE_OPCODE;
  rx_cmd_buff_o->data[DATA_START_INDEX] = subpage_id;
  const size_t INDEX_OFFSET = (DATA_START_INDEX+1);
  for(size_t i=0; i<BYTES_PER_CMD; i++) {
    rx_cmd_buff_o->data[i+INDEX_OFFSET] = subpage_data[i];
  }
  rx_cmd_buff_o->state = RX_CMD_BUFF_STATE_COMPLETE;
}

//// Calls flash write commands to write the data
int bootloader_write_data(rx_cmd_buff_t* rx_cmd_buff) {
  if(rx_cmd_buff->state==RX_CMD_BUFF_STATE_COMPLETE) {
    uint32_t subpage_id = (uint32_t)(rx_cmd_buff->data[DATA_START_INDEX]);
    uint32_t start_addr = APP_ADDR+subpage_id*BYTES_PER_CMD;
    for(size_t i=0; i<BYTES_PER_CMD; i+=8) {
      uint64_t dword = *(uint64_t*)((rx_cmd_buff->data)+DATA_START_INDEX+1+i);
      flash_wait_for_last_operation();
      FLASH_CR |= FLASH_CR_PG;
      MMIO32(i+start_addr)   = (uint32_t)(dword);
      MMIO32(i+start_addr+4) = (uint32_t)(dword >> 32);
      flash_wait_for_last_operation();
      FLASH_CR &= ~FLASH_CR_PG;
      flash_clear_status_flags();
    }
    return 1;
  } else {
    return 0;
  }
}
